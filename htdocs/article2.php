                           <h2 class="blog_entry__title"><a href="">5 Secrets to Being Happier Without Relying on Your Partner</a></h2>
                     <p class="blog_entry__publish_info text-teaser-byline d-none d-sm-block">By <a href="">Kathleen R. Bogart Ph.D.</a> on April 29, 2022 in <a href="">Disability Is Diversity</a></p>
                             <p class="blog_entry__teaser d-none d-sm-block">Every relationship has its ups and downs. In fact, life has its ups and downs.

     That’s totally normal—we wouldn’t be human if we did not have these mood swings.

     At times, you don’t even know why you feel down but you find yourself dreading getting up in the morning and are having trouble getting through the day. In any case, a little mood booster would be a good thing.</p>
                         </div>
               </div>
               <div class="field-name-body">
                   <p><a href="https://www.psychologytoday.com/intl/basics/happiness" title="Psychology Today looks at Happiness" class="basics-link" hreflang="en">Happiness</a> is a popular topic these days. I have even written an upcoming book about it (<em>Growing Happy Kids</em>), and just watched a new movie: <em>The Happy Movie</em>. Although, I didn't find this film to cover the deepest reasons for becoming happier no matter what I found it a strong documentary about how we can do things every day that make us happier. I am going to share some of the <strong>Top 5 Things</strong> we can do daily to become happier.</p>
<div class="markup-replacement-slot markup-replacement-slot-0" data-slot-position="0"></div>
<p><strong>1. </strong><strong>Exercise</strong>. Seems pretty simple but the truth is that the more we exercise from taking daily walks to sweating at the gym—this movement actually releases feel-good endorphins from our brain making us happier. Even a short 5 minute break walking away from your desk, getting some fresh air and strolling around the block can boost your mood. So think about how you can add just a little more movement into your daily (or weekly) life especially if it includes something like a few morning yoga poses or evening walk.</p>
<div class="markup-replacement-slot markup-replacement-slot-1" data-slot-position="1"></div>
<p><strong>2. </strong><strong>Get Immersed</strong>. Mihaly Csikszentmihalyi, the well-regarded psychologist and rock climber, found that the more you immersed into something you love (he termed it flow) the happier you are. This thing you love also requires skill and it is here where you meet that challenge. So do you give yourself permission to get immersed into something you love each day? It may be reading, playing the guitar, creating a website, writing a blog, or cooking a fabulous dinner. Whatever it is for you (it's writing for me) my suggestion is to do more of it.</p>
<div class="markup-replacement-slot markup-replacement-slot-2" data-slot-position="2"></div>
<p><strong>3. </strong><strong>Laugh</strong>. One thing I do for sure every day is laugh. Just today, my eight-year-old client said, "My mom is a big fat weenie" and this was her unique way of expressing herself and I laughed. Because we've all had that feeling that someone just doesn't get us and we can cry or laugh. <a href="https://www.psychologytoday.com/intl/basics/laughter" title="Psychology Today looks at Laughter" class="basics-link" hreflang="en">Laughter</a> changes the energy of a situation immediately and lets us become lighter. So when I need a laugh I go to youtube.com and put in my favorite comedian and give myself permission to laugh-out-loud for just a few minutes before my next client arrives.</p>
<div class="markup-replacement-slot markup-replacement-slot-3" data-slot-position="3"></div>
<p><strong>4. </strong><strong>Meditate</strong>. Richard Davidson, Director of the Lab for Affective <a href="https://www.psychologytoday.com/intl/basics/neuroscience" title="Psychology Today looks at Neuroscience" class="basics-link" hreflang="en">Neuroscience</a>, has studied how <a href="https://www.psychologytoday.com/intl/basics/meditation" title="Psychology Today looks at meditation" class="basics-link" hreflang="en">meditation</a> affects the working of the brain, and how those changes impact emotional health (happiness). He states, "Emotions—and happiness in particular —should be thought of in the same way as a motor skill. They can be trained." Dr. Davidson underscores how even a few minutes a day of meditation can begin to have a positive impact on your brain and sense of well-being. Oprah says she meditates twice a day. I aim for once a day where I can sit quietly, and relax into the knowing that all is really well (despite how it sometimes looks, feels, appears like ...).</p>
<div class="markup-replacement-slot markup-replacement-slot-4" data-slot-position="4"></div>
<p><strong>5. </strong><strong>Become part of a community</strong>. Research shows that by getting involved as part of a community you become happier. You gain a sense of emotional support, and connection with others that fosters a greater sense of well-being. In <em>The Happy Movie</em>, the community in Japan of Okinawa was shown with the 3rd greatest <a href="https://www.psychologytoday.com/intl/basics/attention" title="Psychology Today looks at concentration" class="basics-link" hreflang="en">concentration</a> of centenarians (people over 100) because beyond <a href="https://www.psychologytoday.com/intl/basics/diet" title="Psychology Today looks at diet" class="basics-link" hreflang="en">diet</a> and other health practices, everyone was part of this close knit (and happy) community.</p>
<div class="markup-replacement-slot markup-replacement-slot-5" data-slot-position="5"></div>
<p>Of course, all of this becomes even more important when we have children because we are showing them by the "clarity of our example" how happiness is created and I promise you - they are watching, listening and beginning to follow in our footsteps so let's committ<strong><em> now</em></strong> to make those steps our best possible ones....perhaps not perfect, but happier day-by-day.</p>
<div class="markup-replacement-slot markup-replacement-slot-6" data-slot-position="6"></div>
<p><strong>Maureen Healy</strong> is a practicing <a href="https://www.psychologytoday.com/intl/basics/parenting" title="Psychology Today looks at parenting" class="basics-link" hreflang="en">parenting</a> and emotional health expert with more than 20 years of global experience. Her new book, <strong><em>Growing Happy Kids: How to Foster Inner <a href="https://www.psychologytoday.com/intl/basics/confidence" title="Psychology Today looks at Confidence" class="basics-link" hreflang="en">Confidence</a>, Success and Happiness</em></strong>, is available wherever books are sold. More info: <a href="http://www.growinghappykids.com/">www.growinghappykids.com</a></p>
