                         <h2 class="blog_entry__title"><a href="article1.php">How a Snapshot of Autism Neglects the Bigger Picture</a></h2>
                   <p class="blog_entry__publish_info text-teaser-byline d-none d-sm-block">By <a href="">Kathleen R. Bogart Ph.D.</a> on April 25, 2022 in <a href="">Disability Is Diversity</a></p>
